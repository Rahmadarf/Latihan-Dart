void main() {
  // Perulangan For
  // i = adalah sebagai index, dimulai dari 1
  // i <= 10 adalah kondisi, selama i kurang dari sama dengan 10
  // i++ adalah iterasi, setiap selesai perulangan i akan bertambah 1
  for (int i = 1; i <= 10; i++) {
    // Operasi Matematika
    int hasil = 9 * i;

    // Menampilkan Hasil
    print("9 x $i = $hasil");
  }
}
