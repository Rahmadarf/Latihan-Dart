void main() {
  // Perulangan For
  // int i = 1 adalah sebagai index, dimulai dari 1
  // i <= 5 adalah kondisi, selama i kurang dari sama dengan 5
  // i++ adalah iterasi, setiap selesai perulangan i akan bertambah 1
  for (int i = 1; i <= 5; i++) {
    // Menampilkan Hasil
    print("Perulangan ke-$i");
  }
}